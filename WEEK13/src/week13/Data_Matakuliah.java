    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        String kode_mk = txtkodemk.getText();
        try {
            String statement = "DELETE FROM tb_matakuliah where kode_mk = ?";
            PreparedStatement pst = conek.getkoneksi().prepareStatement(statement);
            pst.setString(1, kode_mk);
            pst.execute();
            txtkodemk.setText("");
            txtmk.setText("");
            txtsks.setText("");
            txtsmt.setText("");
            txtjml.setText("");
            JOptionPane.showMessageDialog(null, "Data berhasil di hapus");
        } catch (SQLException | NumberFormatException | HeadlessException t) {
            JOptionPane.showMessageDialog(null, "Data gagal di hapus");
            System.out.println(t.getMessage());
        }
        datatable();
    }//GEN-LAST:event_btnhapusActionPerformed

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String kode_mk = txtkodemk.getText();
        String mk = txtmk.getText();
        String sks = txtsks.getText();
        String smt = txtsmt.getText();
        String jml_mhs = txtjml.getText();
        
        try {
            // statement / perintah
            String statement = "INSERT INTO tb_matakuliah (kode_mk, mk, sks, smt, jml_mhs) "
                    + "VALUES(?,?,?,?,?)";
            PreparedStatement pst = conek.getkoneksi().prepareStatement(statement);
            pst.setString(1, kode_mk);
            pst.setString(2, mk);
            pst.setString(3, sks);
            pst.setString(4, smt);
            pst.setString(5, jml_mhs);
            
            pst.execute();
            JOptionPane.showMessageDialog(this, "Tambah data matakuliah berhasil!");
            datatable();
        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Tambah data matakuliah gagal!");
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

