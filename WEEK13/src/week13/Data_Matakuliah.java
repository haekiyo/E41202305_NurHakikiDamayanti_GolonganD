        public void datatable() {

        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Kode Matakuliah");
        tbl.addColumn("Matakuliah");
        tbl.addColumn("SKS");
        tbl.addColumn("SMT");
        tbl.addColumn("Jumlah Mahasiswa");
        table.setModel(tbl);
        try {
            Statement statement= (Statement)conek.getkoneksi().createStatement(); {
            ResultSet res=statement.executeQuery("select * from tb_matakuliah");
                tbl.addRow(new Object[]{
                    res.getString("kode_mk"),
                    res.getString("mk"),
                    res.getString("sks"),
                    res.getString("smt"),
                    res.getString("jml_mhs"),});
                table.setModel(tbl);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "berhasil");
        }
    }

